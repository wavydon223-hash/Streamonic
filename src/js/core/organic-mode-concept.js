// ============================================================
// ORGANIC MODE — Concept Definition (Internal Documentation)
// ============================================================
//
// STATUS: Concept only — NO logic, UI, or data models implemented yet.
//
// --- Overview ---
// Organic Mode is a long-term community-first strategy focused on
// growing an engaged, loyal audience through consistent content,
// authentic storytelling, and platform-native behavior. Unlike
// Aggressive Mode (which pushes paid growth) or Viral Mode
// (which chases short-term reach), Organic Mode builds a
// sustainable fanbase through genuine connection.
//
// --- Focus Platforms ---
// - TikTok          — Short-form discovery and trend participation
// - Instagram       — Visual storytelling, carousels, Reels, Stories
// - YouTube Shorts  — Bite-sized content for discovery
// - YouTube Long    — Deep-dive content, vlogs, behind-the-scenes
// - Discord         — Community hub, direct fan interaction
// - Email           — Newsletter, exclusive content, direct line
//
// --- Objective ---
// Grow an engaged audience through daily content, authenticity,
// and platform-native behavior. Prioritize depth of connection
// over breadth of reach.
//
// --- Core Ideas ---
//
// 1. CONTENT CADENCE
//    Establish a sustainable posting rhythm:
//    - TikTok: 1-2x daily (trends + original content)
//    - Instagram: Daily Stories, 3-4 feed posts/week
//    - YouTube Shorts: 3-5x/week
//    - YouTube Long: 1x/week or bi-weekly
//    - Discord: Daily check-ins, weekly events
//    - Email: Weekly newsletter
//
// 2. COMMUNITY LOOPS
//    Create self-sustaining engagement cycles:
//    - Respond to every comment within 2 hours
//    - Feature fan content weekly (reposts, shoutouts)
//    - Discord events: listening parties, Q&As, game nights
//    - User-generated content challenges
//    - "Fan of the Week" spotlights
//
// 3. STORYTELLING ARCS
//    Multi-post narratives that unfold over weeks:
//    - Studio diary series (song creation process)
//    - "Week in the life" recurring format
//    - Album/EP rollout storylines with cliffhangers
//    - Personal growth narratives tied to music themes
//
// 4. EVERGREEN CONTENT
//    Content that remains relevant and discoverable:
//    - "How I made this track" breakdowns
//    - Gear/tutorial content for aspiring producers
//    - Genre education and music theory tips
//    - "Best of" compilations and playlists
//    - Reaction videos to industry moments
//
// 5. PLATFORM RITUALS
//    Consistent recurring content unique to each platform:
//    - TikTok: "Soundcheck Sunday" — preview new snippets
//    - Instagram: "Midweek Motivation" — lyric card + story
//    - YouTube: "Full Session Friday" — live performance clips
//    - Discord: "Monday Morning Drop" — week's schedule reveal
//    - Email: "Weekend Wrap" — curated links and exclusives
//
// --- Key Outputs ---
//
// 1. WEEKLY CONTENT PLAN
//    Detailed 7-day content calendar:
//    - Platform-specific posting schedule
//    - Content type per post (video, image, text, poll)
//    - Caption drafts and hashtag strategy
//    - Cross-posting and repurposing plan
//
// 2. COMMUNITY ENGAGEMENT PLAN
//    Daily/weekly engagement activities:
//    - Comment response windows
//    - Fan feature schedule
//    - Discord event calendar
//    - DM/email outreach targets
//
// 3. STORYTELLING ARCS
//    Multi-week narrative plans:
//    - Arc theme and duration
//    - Episode breakdown per platform
//    - Cliffhanger and payoff points
//    - Audience participation hooks
//
// 4. EVERGREEN CONTENT LIST
//    Library of lasting-value content:
//    - Tutorial topics and formats
//    - "Best of" compilation ideas
//    - FAQ-style content pieces
//    - Seasonal/timely update schedule
//
// --- Platforms Strategy ---
//
// TikTok:       Trend participation + original hooks, duets, stitches
// Instagram:     Aesthetic feed, Stories for intimacy, Reels for reach
// YouTube Shorts: Hook-driven discovery clips, series format
// YouTube Long:  Deep content, vlogs, live sessions, documentaries
// Discord:       Real-time community, exclusive drops, listening parties
// Email:         Personal connection, exclusives, behind-the-scenes
//
// --- Key Metrics ---
// - Save rate (content worth revisiting)
// - Comment rate (conversation depth)
// - Watch time / completion rate (content quality)
// - Returning viewers (loyalty indicator)
// - Community growth rate (Discord members, email subscribers)
// - Engagement velocity (speed of comments/likes after posting)
// - Story completion rate (Instagram Stories)
//
// --- Tone & Style ---
// Authentic, conversational, warm, and consistent. Write like
// you're talking to a friend. Show vulnerability. Celebrate the
// small wins. Make fans feel like insiders, not an audience.
// Quality and consistency over frequency — better to post less
// and mean it than to flood feeds with noise.
//
