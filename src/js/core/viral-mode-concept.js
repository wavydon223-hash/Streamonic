// --- Overview ---
// Viral Mode is a marketing strategy mode focused on maximizing
// short-form video reach, shareability, and hook-based virality
// across TikTok, Instagram Reels, and YouTube Shorts.
//
// --- Focus Platforms ---
// - TikTok    — Primary platform for viral content discovery
// - Reels     — Instagram's short-form video feed
// - Shorts    — YouTube's vertical short-form format
//
// --- Objective ---
// Maximize short-form reach, shareability, and hook-based virality.
// The goal is rapid organic distribution through algorithmic
// amplification rather than paid spend.
//
// --- Core Ideas ---
//
// 1. HOOK EXTRACTION
//    Identify and optimize the first 1-3 seconds of content
//    that determines whether a viewer keeps watching or scrolls.
//    - Test multiple hook variations (text overlays, sound cues, visual surprises)
//    - Analyze retention curves to find optimal hook placement
//    - A/B test cold-open vs. teaser vs. mid-roll hooks
//
// 2. TREND MATCHING
//    Align content with current platform trends:
//    - Trending sounds / audio clips
//    - Popular formats (challenges, transitions, POVs)
//    - Hashtag momentum tracking
//    - Seasonal / cultural moment alignment
//
// 3. SHORT-FORM SCRIPTS
//    Pre-written script templates optimized for 15-60s formats:
//    - "Wait for it..." suspense hooks
//    - "POV: You just discovered..." framing
//    - "Rating X out of 10" engagement bait
//    - "This [genre] just leveled up" genre-specific hooks
//    - Transformation / before-after reveals
//
// 4. INFLUENCER AMPLIFICATION
//    Leverage creator networks for viral distribution:
//    - Micro-influencer seeding (10K-50K followers)
//    - Stitch / Duet / Remix collaboration chains
//    - Creator challenge launches
//    - Comment-section seeding for algorithmic boost
//
// 5. VIRAL ADS
//    Paid amplification of organic viral content:
//    - Spark Ads (TikTok) — boost organic posts
//    - Reels Ads with native creative
//    - Shorts Ads with skip-friendly hooks
//    - Dark post testing for creative variants
//
// --- Key Metrics (Future Implementation) ---
// - Hook completion rate (>50% = strong)
// - Share rate (>2% = viral signal)
// - Saves-to-views ratio
// - Comment velocity (comments per hour post-launch)
// - Duet/Stitch creation count
// - Watch-through rate on 15s/30s/60s content
//
// --- Content Pillars ---
// 1. Music-first clips (song hooks as the star)
// 2. Behind-the-scenes raw moments
// 3. Challenge / participation prompts
// 4. Reaction bait (controversial, surprising, emotional)
// 5. Trend-jacking (remix existing viral formats)
//
// --- Tone & Style ---
// Fast cuts, bold text overlays, high energy,
// authentic/relatable over polished, native-looking
// (not ad-like), sound-on optimized.
//
