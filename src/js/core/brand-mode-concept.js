// --- Overview ---
// Brand Mode is a long-term marketing strategy focused on building
// brand equity, emotional connection, and a recognizable artist
// identity. Unlike Viral Mode (which chases short-term reach),
// Brand Mode prioritizes storytelling, visual consistency, and
// deep audience relationships.
//
// --- Focus Platforms ---
// - Instagram       — Visual storytelling, aesthetic grid, Reels
// - YouTube         — Long-form content, vlogs, behind-the-scenes
// - TikTok          — Brand-aligned short-form (not viral-chasing)
// - Spotify Canvas  — 8-second looping visuals on tracks
// - Press Kits      — Professional media packages for coverage
//
// --- Objective ---
// Build long-term brand equity, emotional connection, and
// recognizable identity that converts casual listeners into
// loyal fans and brand advocates.
//
// --- Core Ideas ---
//
// 1. BRAND PILLARS
//    Define 3-5 non-negotiable themes that all content aligns with.
//    Examples: authenticity, creative evolution, community, resilience.
//    Every piece of content should reinforce at least one pillar.
//
// 2. VISUAL IDENTITY
//    Consistent color palette, typography, photography style,
//    and graphic language across all platforms and materials.
//    - Mood board creation
//    - Brand guidelines document
//    - Template system for social posts
//
// 3. NARRATIVE THEMES
//    Recurring storylines that give the brand depth and continuity:
//    - Origin story and artistic journey
//    - Creative process transparency
//    - Cultural commentary and values
//    - Fan community spotlight
//
// 4. AUDIENCE PERSONA
//    Detailed profile of the ideal fan:
//    - Demographics, psychographics, values
//    - Content consumption habits
//    - Emotional triggers and aspirations
//    - Where they spend time online
//
// 5. CONTENT TONE
//    The emotional register of all communications:
//    - Warm vs. edgy vs. introspective vs. celebratory
//    - Language style (formal, casual, poetic, conversational)
//    - Humor level and boundaries
//    - Response style to criticism and feedback
//
// --- Key Outputs ---
//
// 1. BRAND STORY
//    A narrative document (500-1000 words) that captures:
//    - Who the artist is at their core
//    - What they stand for
//    - Why they make music
//    - What makes them different
//
// 2. CONTENT PILLARS
//    3-5 thematic categories that organize all content:
//    - Example: Music, Lifestyle, Behind-the-Scenes, Community, Culture
//
// 3. VISUAL DIRECTION
//    A mood board and style guide covering:
//    - Color palette (primary, secondary, accent)
//    - Typography hierarchy
//    - Photography style (lighting, composition, editing)
//    - Graphic elements and patterns
//
// 4. MESSAGING GUIDE
//    A reference for consistent communication:
//    - Key phrases and taglines
//    - Bio templates (short + long)
//    - Press release voice and format
//    - Social media caption formulas
//
// 5. IDENTITY MAP
//    A visual matrix showing how brand elements connect:
//    - Values → Visual style → Content themes → Platform strategy
//    - Artist persona → Fan persona → Communication tone
//
// --- Platforms Strategy ---
//
// Instagram:    Curated grid, Stories for intimacy, Reels for reach
// YouTube:      Long-form storytelling, vlogs, premiere events
// TikTok:       Brand-aligned short content (not trend-chasing)
// Spotify:      Canvas loops, editorial pitch, playlist curation
// Press:        Professional kits, media one-sheets, interview prep
//
// --- Key Metrics ---
// - Save rate (listener intent and loyalty)
// - Profile visits (curiosity and discovery)
// - Follower growth rate (organic, sustained)
// - Brand consistency score (visual + tonal alignment across assets)
// - Fan sentiment analysis (positive/negative ratio)
// - Content saves vs. shares (depth of engagement)
//
// --- Tone & Style ---
// Intentional, cohesive, emotionally resonant. Every touchpoint
// should feel like it came from the same artist. Quality over
// quantity. Depth over virality. Legacy over hype.
//
